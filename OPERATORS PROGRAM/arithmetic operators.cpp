#include<iostream>
using namespace std;

int main()
{
    int a, b;

    cout<<"ENTER VALUE OF A: ";
    cin>>a;
    cout<<"ENTER VALUE OF B: ";
    cin>>b;

    cout<<"\nADD: "<<a+b;
    cout<<"\nSUB: "<<a-b;
    cout<<"\nMUL: "<<a*b;
    cout<<"\nDIV: "<<a/b;
    cout<<"\nMOD: "<<a%b;
    cout<<"\nINC: "<<a++;
    cout<<"\nDEC: "<<a--;

    return 0;
}
